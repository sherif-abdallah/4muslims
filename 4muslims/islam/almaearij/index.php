<p class="card-text" style="text-align: center; font-size: 29px;" >
سَأَلَ سَآئِلٌۢ بِعَذَابٍۢ وَاقِعٍۢ ﴿١﴾ لِّلْكَٰفِرِينَ لَيْسَ لَهُۥ دَافِعٌ ﴿٢﴾ مِّنَ ٱللَّهِ ذِى ٱلْمَعَارِجِ ﴿٣﴾ تَعْرُجُ ٱلْمَلَٰٓئِكَةُ وَٱلرُّوحُ إِلَيْهِ فِى يَوْمٍۢ كَانَ مِقْدَارُهُۥ خَمْسِينَ أَلْفَ سَنَةٍۢ ﴿٤﴾ فَٱصْبِرْ صَبْرًا جَمِيلًا ﴿٥﴾ إِنَّهُمْ يَرَوْنَهُۥ بَعِيدًا ﴿٦﴾ وَنَرَىٰهُ قَرِيبًا ﴿٧﴾ يَوْمَ تَكُونُ ٱلسَّمَآءُ كَٱلْمُهْلِ ﴿٨﴾ وَتَكُونُ ٱلْجِبَالُ كَٱلْعِهْنِ ﴿٩﴾ وَلَا يَسْـَٔلُ حَمِيمٌ حَمِيمًا ﴿١٠﴾ يُبَصَّرُونَهُمْ ۚ يَوَدُّ ٱلْمُجْرِمُ لَوْ يَفْتَدِى مِنْ عَذَابِ يَوْمِئِذٍ بِبَنِيهِ ﴿١١﴾ وَصَٰحِبَتِهِۦ وَأَخِيهِ ﴿١٢﴾ وَفَصِيلَتِهِ ٱلَّتِى تُـْٔوِيهِ ﴿١٣﴾ وَمَن فِى ٱلْأَرْضِ جَمِيعًا ثُمَّ يُنجِيهِ ﴿١٤﴾ كَلَّآ ۖ إِنَّهَا لَظَىٰ ﴿١٥﴾ نَزَّاعَةً لِّلشَّوَىٰ ﴿١٦﴾ تَدْعُوا مَنْ أَدْبَرَ وَتَوَلَّىٰ ﴿١٧﴾ وَجَمَعَ فَأَوْعَىٰٓ ﴿١٨﴾ ۞ إِنَّ ٱلْإِنسَٰنَ خُلِقَ هَلُوعًا ﴿١٩﴾ إِذَا مَسَّهُ ٱلشَّرُّ جَزُوعًا ﴿٢٠﴾ وَإِذَا مَسَّهُ ٱلْخَيْرُ مَنُوعًا ﴿٢١﴾ إِلَّا ٱلْمُصَلِّينَ ﴿٢٢﴾ ٱلَّذِينَ هُمْ عَلَىٰ صَلَاتِهِمْ دَآئِمُونَ ﴿٢٣﴾ وَٱلَّذِينَ فِىٓ أَمْوَٰلِهِمْ حَقٌّ مَّعْلُومٌ ﴿٢٤﴾ لِّلسَّآئِلِ وَٱلْمَحْرُومِ ﴿٢٥﴾ وَٱلَّذِينَ يُصَدِّقُونَ بِيَوْمِ ٱلدِّينِ ﴿٢٦﴾ وَٱلَّذِينَ هُم مِّنْ عَذَابِ رَبِّهِم مُّشْفِقُونَ ﴿٢٧﴾ إِنَّ عَذَابَ رَبِّهِمْ غَيْرُ مَأْمُونٍۢ ﴿٢٨﴾ وَٱلَّذِينَ هُمْ لِفُرُوجِهِمْ حَٰفِظُونَ ﴿٢٩﴾ إِلَّا عَلَىٰٓ أَزْوَٰجِهِمْ أَوْ مَا مَلَكَتْ أَيْمَٰنُهُمْ فَإِنَّهُمْ غَيْرُ مَلُومِينَ ﴿٣٠﴾ فَمَنِ ٱبْتَغَىٰ وَرَآءَ ذَٰلِكَ فَأُولَٰٓئِكَ هُمُ ٱلْعَادُونَ ﴿٣١﴾ وَٱلَّذِينَ هُمْ لِأَمَٰنَٰتِهِمْ وَعَهْدِهِمْ رَٰعُونَ ﴿٣٢﴾ وَٱلَّذِينَ هُم بِشَهَٰدَٰتِهِمْ قَآئِمُونَ ﴿٣٣﴾ وَٱلَّذِينَ هُمْ عَلَىٰ صَلَاتِهِمْ يُحَافِظُونَ ﴿٣٤﴾ أُولَٰٓئِكَ فِى جَنَّٰتٍۢ مُّكْرَمُونَ ﴿٣٥﴾ فَمَالِ ٱلَّذِينَ كَفَرُوا قِبَلَكَ مُهْطِعِينَ ﴿٣٦﴾ عَنِ ٱلْيَمِينِ وَعَنِ ٱلشِّمَالِ عِزِينَ ﴿٣٧﴾ أَيَطْمَعُ كُلُّ ٱمْرِئٍۢ مِّنْهُمْ أَن يُدْخَلَ جَنَّةَ نَعِيمٍۢ ﴿٣٨﴾ كَلَّآ ۖ إِنَّا خَلَقْنَٰهُم مِّمَّا يَعْلَمُونَ ﴿٣٩﴾ فَلَآ أُقْسِمُ بِرَبِّ ٱلْمَشَٰرِقِ وَٱلْمَغَٰرِبِ إِنَّا لَقَٰدِرُونَ ﴿٤٠﴾ عَلَىٰٓ أَن نُّبَدِّلَ خَيْرًا مِّنْهُمْ وَمَا نَحْنُ بِمَسْبُوقِينَ ﴿٤١﴾ فَذَرْهُمْ يَخُوضُوا وَيَلْعَبُوا حَتَّىٰ يُلَٰقُوا يَوْمَهُمُ ٱلَّذِى يُوعَدُونَ ﴿٤٢﴾ يَوْمَ يَخْرُجُونَ مِنَ ٱلْأَجْدَاثِ سِرَاعًا كَأَنَّهُمْ إِلَىٰ نُصُبٍۢ يُوفِضُونَ ﴿٤٣﴾ خَٰشِعَةً أَبْصَٰرُهُمْ تَرْهَقُهُمْ ذِلَّةٌ ۚ ذَٰلِكَ ٱلْيَوْمُ ٱلَّذِى كَانُوا يُوعَدُونَ
