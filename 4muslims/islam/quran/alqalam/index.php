<p class="card-text" style="text-align: center; font-size: 29px;" >
نٓ ۚ وَٱلْقَلَمِ وَمَا يَسْطُرُونَ ﴿١﴾ مَآ أَنتَ بِنِعْمَةِ رَبِّكَ بِمَجْنُونٍۢ ﴿٢﴾ وَإِنَّ لَكَ لَأَجْرًا غَيْرَ مَمْنُونٍۢ ﴿٣﴾ وَإِنَّكَ لَعَلَىٰ خُلُقٍ عَظِيمٍۢ ﴿٤﴾ فَسَتُبْصِرُ وَيُبْصِرُونَ ﴿٥﴾ بِأَييِّكُمُ ٱلْمَفْتُونُ ﴿٦﴾ إِنَّ رَبَّكَ هُوَ أَعْلَمُ بِمَن ضَلَّ عَن سَبِيلِهِۦ وَهُوَ أَعْلَمُ بِٱلْمُهْتَدِينَ ﴿٧﴾ فَلَا تُطِعِ ٱلْمُكَذِّبِينَ ﴿٨﴾ وَدُّوا لَوْ تُدْهِنُ فَيُدْهِنُونَ ﴿٩﴾ وَلَا تُطِعْ كُلَّ حَلَّافٍۢ مَّهِينٍ ﴿١٠﴾ هَمَّازٍۢ مَّشَّآءٍ بِنَمِيمٍۢ ﴿١١﴾ مَّنَّاعٍۢ لِّلْخَيْرِ مُعْتَدٍ أَثِيمٍ ﴿١٢﴾ عُتُلٍّ بَعْدَ ذَٰلِكَ زَنِيمٍ ﴿١٣﴾ أَن كَانَ ذَا مَالٍۢ وَبَنِينَ ﴿١٤﴾ إِذَا تُتْلَىٰ عَلَيْهِ ءَايَٰتُنَا قَالَ أَسَٰطِيرُ ٱلْأَوَّلِينَ ﴿١٥﴾ سَنَسِمُهُۥ عَلَى ٱلْخُرْطُومِ ﴿١٦﴾ إِنَّا بَلَوْنَٰهُمْ كَمَا بَلَوْنَآ أَصْحَٰبَ ٱلْجَنَّةِ إِذْ أَقْسَمُوا لَيَصْرِمُنَّهَا مُصْبِحِينَ ﴿١٧﴾ وَلَا يَسْتَثْنُونَ ﴿١٨﴾ فَطَافَ عَلَيْهَا طَآئِفٌ مِّن رَّبِّكَ وَهُمْ نَآئِمُونَ ﴿١٩﴾ فَأَصْبَحَتْ كَٱلصَّرِيمِ ﴿٢٠﴾ فَتَنَادَوْا مُصْبِحِينَ ﴿٢١﴾ أَنِ ٱغْدُوا عَلَىٰ حَرْثِكُمْ إِن كُنتُمْ صَٰرِمِينَ ﴿٢٢﴾ فَٱنطَلَقُوا وَهُمْ يَتَخَٰفَتُونَ ﴿٢٣﴾ أَن لَّا يَدْخُلَنَّهَا ٱلْيَوْمَ عَلَيْكُم مِّسْكِينٌ ﴿٢٤﴾ وَغَدَوْا عَلَىٰ حَرْدٍۢ قَٰدِرِينَ ﴿٢٥﴾ فَلَمَّا رَأَوْهَا قَالُوٓا إِنَّا لَضَآلُّونَ ﴿٢٦﴾ بَلْ نَحْنُ مَحْرُومُونَ ﴿٢٧﴾ قَالَ أَوْسَطُهُمْ أَلَمْ أَقُل لَّكُمْ لَوْلَا تُسَبِّحُونَ ﴿٢٨﴾ قَالُوا سُبْحَٰنَ رَبِّنَآ إِنَّا كُنَّا ظَٰلِمِينَ ﴿٢٩﴾ فَأَقْبَلَ بَعْضُهُمْ عَلَىٰ بَعْضٍۢ يَتَلَٰوَمُونَ ﴿٣٠﴾ قَالُوا يَٰوَيْلَنَآ إِنَّا كُنَّا طَٰغِينَ ﴿٣١﴾ عَسَىٰ رَبُّنَآ أَن يُبْدِلَنَا خَيْرًا مِّنْهَآ إِنَّآ إِلَىٰ رَبِّنَا رَٰغِبُونَ ﴿٣٢﴾ كَذَٰلِكَ ٱلْعَذَابُ ۖ وَلَعَذَابُ ٱلْءَاخِرَةِ أَكْبَرُ ۚ لَوْ كَانُوا يَعْلَمُونَ ﴿٣٣﴾ إِنَّ لِلْمُتَّقِينَ عِندَ رَبِّهِمْ جَنَّٰتِ ٱلنَّعِيمِ ﴿٣٤﴾ أَفَنَجْعَلُ ٱلْمُسْلِمِينَ كَٱلْمُجْرِمِينَ ﴿٣٥﴾ مَا لَكُمْ كَيْفَ تَحْكُمُونَ ﴿٣٦﴾ أَمْ لَكُمْ كِتَٰبٌ فِيهِ تَدْرُسُونَ ﴿٣٧﴾ إِنَّ لَكُمْ فِيهِ لَمَا تَخَيَّرُونَ ﴿٣٨﴾ أَمْ لَكُمْ أَيْمَٰنٌ عَلَيْنَا بَٰلِغَةٌ إِلَىٰ يَوْمِ ٱلْقِيَٰمَةِ ۙ إِنَّ لَكُمْ لَمَا تَحْكُمُونَ ﴿٣٩﴾ سَلْهُمْ أَيُّهُم بِذَٰلِكَ زَعِيمٌ ﴿٤٠﴾ أَمْ لَهُمْ شُرَكَآءُ فَلْيَأْتُوا بِشُرَكَآئِهِمْ إِن كَانُوا صَٰدِقِينَ ﴿٤١﴾ يَوْمَ يُكْشَفُ عَن سَاقٍۢ وَيُدْعَوْنَ إِلَى ٱلسُّجُودِ فَلَا يَسْتَطِيعُونَ ﴿٤٢﴾ خَٰشِعَةً أَبْصَٰرُهُمْ تَرْهَقُهُمْ ذِلَّةٌ ۖ وَقَدْ كَانُوا يُدْعَوْنَ إِلَى ٱلسُّجُودِ وَهُمْ سَٰلِمُونَ ﴿٤٣﴾ فَذَرْنِى وَمَن يُكَذِّبُ بِهَٰذَا ٱلْحَدِيثِ ۖ سَنَسْتَدْرِجُهُم مِّنْ حَيْثُ لَا يَعْلَمُونَ ﴿٤٤﴾ وَأُمْلِى لَهُمْ ۚ إِنَّ كَيْدِى مَتِينٌ ﴿٤٥﴾ أَمْ تَسْـَٔلُهُمْ أَجْرًا فَهُم مِّن مَّغْرَمٍۢ مُّثْقَلُونَ ﴿٤٦﴾ أَمْ عِندَهُمُ ٱلْغَيْبُ فَهُمْ يَكْتُبُونَ ﴿٤٧﴾ فَٱصْبِرْ لِحُكْمِ رَبِّكَ وَلَا تَكُن كَصَاحِبِ ٱلْحُوتِ إِذْ نَادَىٰ وَهُوَ مَكْظُومٌ ﴿٤٨﴾ لَّوْلَآ أَن تَدَٰرَكَهُۥ نِعْمَةٌ مِّن رَّبِّهِۦ لَنُبِذَ بِٱلْعَرَآءِ وَهُوَ مَذْمُومٌ ﴿٤٩﴾ فَٱجْتَبَٰهُ رَبُّهُۥ فَجَعَلَهُۥ مِنَ ٱلصَّٰلِحِينَ ﴿٥٠﴾ وَإِن يَكَادُ ٱلَّذِينَ كَفَرُوا لَيُزْلِقُونَكَ بِأَبْصَٰرِهِمْ لَمَّا سَمِعُوا ٱلذِّكْرَ وَيَقُولُونَ إِنَّهُۥ لَمَجْنُونٌ ﴿٥١﴾ وَمَا هُوَ إِلَّا ذِكْرٌ لِّلْعَٰلَمِينَ
