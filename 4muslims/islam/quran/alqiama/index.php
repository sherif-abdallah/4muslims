لَآ أُقْسِمُ بِيَوْمِ ٱلْقِيَٰمَةِ ﴿١﴾ وَلَآ أُقْسِمُ بِٱلنَّفْسِ ٱللَّوَّامَةِ ﴿٢﴾ أَيَحْسَبُ ٱلْإِنسَٰنُ أَلَّن نَّجْمَعَ عِظَامَهُۥ ﴿٣﴾ بَلَىٰ قَٰدِرِينَ عَلَىٰٓ أَن نُّسَوِّىَ بَنَانَهُۥ ﴿٤﴾ بَلْ يُرِيدُ ٱلْإِنسَٰنُ لِيَفْجُرَ أَمَامَهُۥ ﴿٥﴾ يَسْـَٔلُ أَيَّانَ يَوْمُ ٱلْقِيَٰمَةِ ﴿٦﴾ فَإِذَا بَرِقَ ٱلْبَصَرُ ﴿٧﴾ وَخَسَفَ ٱلْقَمَرُ ﴿٨﴾ وَجُمِعَ ٱلشَّمْسُ وَٱلْقَمَرُ ﴿٩﴾ يَقُولُ ٱلْإِنسَٰنُ يَوْمَئِذٍ أَيْنَ ٱلْمَفَرُّ ﴿١٠﴾ كَلَّا لَا وَزَرَ ﴿١١﴾ إِلَىٰ رَبِّكَ يَوْمَئِذٍ ٱلْمُسْتَقَرُّ ﴿١٢﴾ يُنَبَّؤُا ٱلْإِنسَٰنُ يَوْمَئِذٍ بِمَا قَدَّمَ وَأَخَّرَ ﴿١٣﴾ بَلِ ٱلْإِنسَٰنُ عَلَىٰ نَفْسِهِۦ بَصِيرَةٌ ﴿١٤﴾ وَلَوْ أَلْقَىٰ مَعَاذِيرَهُۥ ﴿١٥﴾ لَا تُحَرِّكْ بِهِۦ لِسَانَكَ لِتَعْجَلَ بِهِۦٓ ﴿١٦﴾ إِنَّ عَلَيْنَا جَمْعَهُۥ وَقُرْءَانَهُۥ ﴿١٧﴾ فَإِذَا قَرَأْنَٰهُ فَٱتَّبِعْ قُرْءَانَهُۥ ﴿١٨﴾ ثُمَّ إِنَّ عَلَيْنَا بَيَانَهُۥ ﴿١٩﴾ كَلَّا بَلْ تُحِبُّونَ ٱلْعَاجِلَةَ ﴿٢٠﴾ وَتَذَرُونَ ٱلْءَاخِرَةَ ﴿٢١﴾ وُجُوهٌ يَوْمَئِذٍۢ نَّاضِرَةٌ ﴿٢٢﴾ إِلَىٰ رَبِّهَا نَاظِرَةٌ ﴿٢٣﴾ وَوُجُوهٌ يَوْمَئِذٍ بَاسِرَةٌ ﴿٢٤﴾ تَظُنُّ أَن يُفْعَلَ بِهَا فَاقِرَةٌ ﴿٢٥﴾ كَلَّآ إِذَا بَلَغَتِ ٱلتَّرَاقِىَ ﴿٢٦﴾ وَقِيلَ مَنْ ۜ رَاقٍۢ ﴿٢٧﴾ وَظَنَّ أَنَّهُ ٱلْفِرَاقُ ﴿٢٨﴾ وَٱلْتَفَّتِ ٱلسَّاقُ بِٱلسَّاقِ ﴿٢٩﴾ إِلَىٰ رَبِّكَ يَوْمَئِذٍ ٱلْمَسَاقُ ﴿٣٠﴾ فَلَا صَدَّقَ وَلَا صَلَّىٰ ﴿٣١﴾ وَلَٰكِن كَذَّبَ وَتَوَلَّىٰ ﴿٣٢﴾ ثُمَّ ذَهَبَ إِلَىٰٓ أَهْلِهِۦ يَتَمَطَّىٰٓ ﴿٣٣﴾ أَوْلَىٰ لَكَ فَأَوْلَىٰ ﴿٣٤﴾ ثُمَّ أَوْلَىٰ لَكَ فَأَوْلَىٰٓ ﴿٣٥﴾ أَيَحْسَبُ ٱلْإِنسَٰنُ أَن يُتْرَكَ سُدًى ﴿٣٦﴾ أَلَمْ يَكُ نُطْفَةً مِّن مَّنِىٍّۢ يُمْنَىٰ ﴿٣٧﴾ ثُمَّ كَانَ عَلَقَةً فَخَلَقَ فَسَوَّىٰ ﴿٣٨﴾ فَجَعَلَ مِنْهُ ٱلزَّوْجَيْنِ ٱلذَّكَرَ وَٱلْأُنثَىٰٓ ﴿٣٩﴾ أَلَيْسَ ذَٰلِكَ بِقَٰدِرٍ عَلَىٰٓ أَن يُحْۦِىَ ٱلْمَوْتَىٰ
