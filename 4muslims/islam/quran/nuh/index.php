<p class="card-text" style="text-align: center; font-size: 29px;" >
إِنَّآ أَرْسَلْنَا نُوحًا إِلَىٰ قَوْمِهِۦٓ أَنْ أَنذِرْ قَوْمَكَ مِن قَبْلِ أَن يَأْتِيَهُمْ عَذَابٌ أَلِيمٌ ﴿١﴾ قَالَ يَٰقَوْمِ إِنِّى لَكُمْ نَذِيرٌ مُّبِينٌ ﴿٢﴾ أَنِ ٱعْبُدُوا ٱللَّهَ وَٱتَّقُوهُ وَأَطِيعُونِ ﴿٣﴾ يَغْفِرْ لَكُم مِّن ذُنُوبِكُمْ وَيُؤَخِّرْكُمْ إِلَىٰٓ أَجَلٍۢ مُّسَمًّى ۚ إِنَّ أَجَلَ ٱللَّهِ إِذَا جَآءَ لَا يُؤَخَّرُ ۖ لَوْ كُنتُمْ تَعْلَمُونَ ﴿٤﴾ قَالَ رَبِّ إِنِّى دَعَوْتُ قَوْمِى لَيْلًا وَنَهَارًا ﴿٥﴾ فَلَمْ يَزِدْهُمْ دُعَآءِىٓ إِلَّا فِرَارًا ﴿٦﴾ وَإِنِّى كُلَّمَا دَعَوْتُهُمْ لِتَغْفِرَ لَهُمْ جَعَلُوٓا أَصَٰبِعَهُمْ فِىٓ ءَاذَانِهِمْ وَٱسْتَغْشَوْا ثِيَابَهُمْ وَأَصَرُّوا وَٱسْتَكْبَرُوا ٱسْتِكْبَارًا ﴿٧﴾ ثُمَّ إِنِّى دَعَوْتُهُمْ جِهَارًا ﴿٨﴾ ثُمَّ إِنِّىٓ أَعْلَنتُ لَهُمْ وَأَسْرَرْتُ لَهُمْ إِسْرَارًا ﴿٩﴾ فَقُلْتُ ٱسْتَغْفِرُوا رَبَّكُمْ إِنَّهُۥ كَانَ غَفَّارًا ﴿١٠﴾ يُرْسِلِ ٱلسَّمَآءَ عَلَيْكُم مِّدْرَارًا ﴿١١﴾ وَيُمْدِدْكُم بِأَمْوَٰلٍۢ وَبَنِينَ وَيَجْعَل لَّكُمْ جَنَّٰتٍۢ وَيَجْعَل لَّكُمْ أَنْهَٰرًا ﴿١٢﴾ مَّا لَكُمْ لَا تَرْجُونَ لِلَّهِ وَقَارًا ﴿١٣﴾ وَقَدْ خَلَقَكُمْ أَطْوَارًا ﴿١٤﴾ أَلَمْ تَرَوْا كَيْفَ خَلَقَ ٱللَّهُ سَبْعَ سَمَٰوَٰتٍۢ طِبَاقًا ﴿١٥﴾ وَجَعَلَ ٱلْقَمَرَ فِيهِنَّ نُورًا وَجَعَلَ ٱلشَّمْسَ سِرَاجًا ﴿١٦﴾ وَٱللَّهُ أَنۢبَتَكُم مِّنَ ٱلْأَرْضِ نَبَاتًا ﴿١٧﴾ ثُمَّ يُعِيدُكُمْ فِيهَا وَيُخْرِجُكُمْ إِخْرَاجًا ﴿١٨﴾ وَٱللَّهُ جَعَلَ لَكُمُ ٱلْأَرْضَ بِسَاطًا ﴿١٩﴾ لِّتَسْلُكُوا مِنْهَا سُبُلًا فِجَاجًا ﴿٢٠﴾ قَالَ نُوحٌ رَّبِّ إِنَّهُمْ عَصَوْنِى وَٱتَّبَعُوا مَن لَّمْ يَزِدْهُ مَالُهُۥ وَوَلَدُهُۥٓ إِلَّا خَسَارًا ﴿٢١﴾ وَمَكَرُوا مَكْرًا كُبَّارًا ﴿٢٢﴾ وَقَالُوا لَا تَذَرُنَّ ءَالِهَتَكُمْ وَلَا تَذَرُنَّ وَدًّا وَلَا سُوَاعًا وَلَا يَغُوثَ وَيَعُوقَ وَنَسْرًا ﴿٢٣﴾ وَقَدْ أَضَلُّوا كَثِيرًا ۖ وَلَا تَزِدِ ٱلظَّٰلِمِينَ إِلَّا ضَلَٰلًا ﴿٢٤﴾ مِّمَّا خَطِيٓـَٰٔتِهِمْ أُغْرِقُوا فَأُدْخِلُوا نَارًا فَلَمْ يَجِدُوا لَهُم مِّن دُونِ ٱللَّهِ أَنصَارًا ﴿٢٥﴾ وَقَالَ نُوحٌ رَّبِّ لَا تَذَرْ عَلَى ٱلْأَرْضِ مِنَ ٱلْكَٰفِرِينَ دَيَّارًا ﴿٢٦﴾ إِنَّكَ إِن تَذَرْهُمْ يُضِلُّوا عِبَادَكَ وَلَا يَلِدُوٓا إِلَّا فَاجِرًا كَفَّارًا ﴿٢٧﴾ رَّبِّ ٱغْفِرْ لِى وَلِوَٰلِدَىَّ وَلِمَن دَخَلَ بَيْتِىَ مُؤْمِنًا وَلِلْمُؤْمِنِينَ وَٱلْمُؤْمِنَٰتِ وَلَا تَزِدِ ٱلظَّٰلِمِينَ إِلَّا تَبَارًۢا
