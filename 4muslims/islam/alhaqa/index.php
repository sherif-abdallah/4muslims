<p class="card-text" style="text-align: center; font-size: 29px;" >
ٱلْحَآقَّةُ ﴿١﴾ مَا ٱلْحَآقَّةُ ﴿٢﴾ وَمَآ أَدْرَىٰكَ مَا ٱلْحَآقَّةُ ﴿٣﴾ كَذَّبَتْ ثَمُودُ وَعَادٌۢ بِٱلْقَارِعَةِ ﴿٤﴾ فَأَمَّا ثَمُودُ فَأُهْلِكُوا بِٱلطَّاغِيَةِ ﴿٥﴾ وَأَمَّا عَادٌ فَأُهْلِكُوا بِرِيحٍۢ صَرْصَرٍ عَاتِيَةٍۢ ﴿٦﴾ سَخَّرَهَا عَلَيْهِمْ سَبْعَ لَيَالٍۢ وَثَمَٰنِيَةَ أَيَّامٍ حُسُومًا فَتَرَى ٱلْقَوْمَ فِيهَا صَرْعَىٰ كَأَنَّهُمْ أَعْجَازُ نَخْلٍ خَاوِيَةٍۢ ﴿٧﴾ فَهَلْ تَرَىٰ لَهُم مِّنۢ بَاقِيَةٍۢ ﴿٨﴾ وَجَآءَ فِرْعَوْنُ وَمَن قَبْلَهُۥ وَٱلْمُؤْتَفِكَٰتُ بِٱلْخَاطِئَةِ ﴿٩﴾ فَعَصَوْا رَسُولَ رَبِّهِمْ فَأَخَذَهُمْ أَخْذَةً رَّابِيَةً ﴿١٠﴾ إِنَّا لَمَّا طَغَا ٱلْمَآءُ حَمَلْنَٰكُمْ فِى ٱلْجَارِيَةِ ﴿١١﴾ لِنَجْعَلَهَا لَكُمْ تَذْكِرَةً وَتَعِيَهَآ أُذُنٌ وَٰعِيَةٌ ﴿١٢﴾ فَإِذَا نُفِخَ فِى ٱلصُّورِ نَفْخَةٌ وَٰحِدَةٌ ﴿١٣﴾ وَحُمِلَتِ ٱلْأَرْضُ وَٱلْجِبَالُ فَدُكَّتَا دَكَّةً وَٰحِدَةً ﴿١٤﴾ فَيَوْمَئِذٍۢ وَقَعَتِ ٱلْوَاقِعَةُ ﴿١٥﴾ وَٱنشَقَّتِ ٱلسَّمَآءُ فَهِىَ يَوْمَئِذٍۢ وَاهِيَةٌ ﴿١٦﴾ وَٱلْمَلَكُ عَلَىٰٓ أَرْجَآئِهَا ۚ وَيَحْمِلُ عَرْشَ رَبِّكَ فَوْقَهُمْ يَوْمَئِذٍۢ ثَمَٰنِيَةٌ ﴿١٧﴾ يَوْمَئِذٍۢ تُعْرَضُونَ لَا تَخْفَىٰ مِنكُمْ خَافِيَةٌ ﴿١٨﴾ فَأَمَّا مَنْ أُوتِىَ كِتَٰبَهُۥ بِيَمِينِهِۦ فَيَقُولُ هَآؤُمُ ٱقْرَءُوا كِتَٰبِيَهْ ﴿١٩﴾ إِنِّى ظَنَنتُ أَنِّى مُلَٰقٍ حِسَابِيَهْ ﴿٢٠﴾ فَهُوَ فِى عِيشَةٍۢ رَّاضِيَةٍۢ ﴿٢١﴾ فِى جَنَّةٍ عَالِيَةٍۢ ﴿٢٢﴾ قُطُوفُهَا دَانِيَةٌ ﴿٢٣﴾ كُلُوا وَٱشْرَبُوا هَنِيٓـًٔۢا بِمَآ أَسْلَفْتُمْ فِى ٱلْأَيَّامِ ٱلْخَالِيَةِ ﴿٢٤﴾ وَأَمَّا مَنْ أُوتِىَ كِتَٰبَهُۥ بِشِمَالِهِۦ فَيَقُولُ يَٰلَيْتَنِى لَمْ أُوتَ كِتَٰبِيَهْ ﴿٢٥﴾ وَلَمْ أَدْرِ مَا حِسَابِيَهْ ﴿٢٦﴾ يَٰلَيْتَهَا كَانَتِ ٱلْقَاضِيَةَ ﴿٢٧﴾ مَآ أَغْنَىٰ عَنِّى مَالِيَهْ ۜ ﴿٢٨﴾ هَلَكَ عَنِّى سُلْطَٰنِيَهْ ﴿٢٩﴾ خُذُوهُ فَغُلُّوهُ ﴿٣٠﴾ ثُمَّ ٱلْجَحِيمَ صَلُّوهُ ﴿٣١﴾ ثُمَّ فِى سِلْسِلَةٍۢ ذَرْعُهَا سَبْعُونَ ذِرَاعًا فَٱسْلُكُوهُ ﴿٣٢﴾ إِنَّهُۥ كَانَ لَا يُؤْمِنُ بِٱللَّهِ ٱلْعَظِيمِ ﴿٣٣﴾ وَلَا يَحُضُّ عَلَىٰ طَعَامِ ٱلْمِسْكِينِ ﴿٣٤﴾ فَلَيْسَ لَهُ ٱلْيَوْمَ هَٰهُنَا حَمِيمٌ ﴿٣٥﴾ وَلَا طَعَامٌ إِلَّا مِنْ غِسْلِينٍۢ ﴿٣٦﴾ لَّا يَأْكُلُهُۥٓ إِلَّا ٱلْخَٰطِـُٔونَ ﴿٣٧﴾ فَلَآ أُقْسِمُ بِمَا تُبْصِرُونَ ﴿٣٨﴾ وَمَا لَا تُبْصِرُونَ ﴿٣٩﴾ إِنَّهُۥ لَقَوْلُ رَسُولٍۢ كَرِيمٍۢ ﴿٤٠﴾ وَمَا هُوَ بِقَوْلِ شَاعِرٍۢ ۚ قَلِيلًا مَّا تُؤْمِنُونَ ﴿٤١﴾ وَلَا بِقَوْلِ كَاهِنٍۢ ۚ قَلِيلًا مَّا تَذَكَّرُونَ ﴿٤٢﴾ تَنزِيلٌ مِّن رَّبِّ ٱلْعَٰلَمِينَ ﴿٤٣﴾ وَلَوْ تَقَوَّلَ عَلَيْنَا بَعْضَ ٱلْأَقَاوِيلِ ﴿٤٤﴾ لَأَخَذْنَا مِنْهُ بِٱلْيَمِينِ ﴿٤٥﴾ ثُمَّ لَقَطَعْنَا مِنْهُ ٱلْوَتِينَ ﴿٤٦﴾ فَمَا مِنكُم مِّنْ أَحَدٍ عَنْهُ حَٰجِزِينَ ﴿٤٧﴾ وَإِنَّهُۥ لَتَذْكِرَةٌ لِّلْمُتَّقِينَ ﴿٤٨﴾ وَإِنَّا لَنَعْلَمُ أَنَّ مِنكُم مُّكَذِّبِينَ ﴿٤٩﴾ وَإِنَّهُۥ لَحَسْرَةٌ عَلَى ٱلْكَٰفِرِينَ ﴿٥٠﴾ وَإِنَّهُۥ لَحَقُّ ٱلْيَقِينِ ﴿٥١﴾ فَسَبِّحْ بِٱسْمِ رَبِّكَ ٱلْعَظِيمِ
